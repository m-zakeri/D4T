    public PaletteColorChooserPreviewPanel() {
        initComponents();
        //setBorder(new VisualMargin(3,0,3,0));
        setToolTipText("on"); // set dummy text, to switch tooltip on
    }

    @Override
    public void paintComponent(Graphics g) {
        Insets insets = getInsets();
        int x = insets.left;
        int y = insets.top;
        int w = getWidth() - insets.left - insets.right;
        int h = getHeight() - insets.top - insets.bottom;
        g.setColor(PREVIEW_BACKGROUND_COLOR);
        g.fillRect(x + 1, y + 1, w - 2, h - 2);
        g.setColor(PREVIEW_BORDER_COLOR);
        g.drawRect(x, y, w - 1, h - 1);
        g.setColor(getForeground());
        g.fillRect(x + 2, y + 2, w - 4, h - 4);
    }

    @Override
    public String getToolTipText(MouseEvent evt) {
        Color color = getForeground();
        return (color == null) ? null : color.getRed() + ", " + color.getGreen() + ", " + color.getBlue();
    }

