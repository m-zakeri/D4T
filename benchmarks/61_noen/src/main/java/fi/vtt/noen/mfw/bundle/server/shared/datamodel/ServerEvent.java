package fi.vtt.noen.mfw.bundle.server.shared.datamodel;

import fi.vtt.noen.mfw.bundle.common.DataObject;
import fi.vtt.noen.mfw.bundle.common.DataType;
import fi.vtt.noen.mfw.bundle.common.EventType;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;
import javax.persistence.Transient;
import java.text.DateFormat;
import java.util.Date;

/**
 * Describes an event, e.g. alert for exceeded threshold or a lost probe.
 * Annotations are for persistence with JPA (DB storage).
 *
 * @author Teemu Kanstren
 */
@Entity
@Table(name="event")
public class ServerEvent extends DataObject {
  //unique identifier, generated by the JPA/DB
  @GeneratedValue
  @Id
  @Column(name="id", nullable=false, updatable=false)
  private Long eventId;
  //the information message of the event, natural language for human consumer
  @Column(name="message")
  private String message;
  //time when the event was observed
  @Column(name="event_time")
  @Temporal(TemporalType.TIMESTAMP)
  private Date time;
  @Column(name="event_type")
  public EventType type;
  @Column(name="event_source")
  private String source;
  @Transient
  private long subscriptionId;

  public enum SortKey {
    MESSAGE, TIME
  }

  public ServerEvent() {
    super(DataType.SERVER_EVENT);
  }

  public ServerEvent(long time, EventType type, String source, String message) {
    super(DataType.SERVER_EVENT);
    this.source = source;
    this.message = message;
    this.time = new Date(time);
    this.type = type;
    this.source = source;
    this.subscriptionId = -1;
  }

  public ServerEvent(long time, EventType type, String source, String message, long subscriptionId) {
    super(DataType.SERVER_EVENT);
    this.source = source;
    this.message = message;
    this.time = new Date(time);
    this.type = type;
    this.source = source;
    this.subscriptionId = subscriptionId;
  }

  public String getMessage() {
    return message;
  }

  public long getTime() {
    return time.getTime();
  }

  public String getSource() {
    return source;
  }

  public long getSubscriptionId() {
    return subscriptionId;
  }

  @Transient
  public String getTimeString() {
    return DateFormat.getDateTimeInstance().format(time);
  }
}
