package fi.vtt.noen.mfw.bundle.common;

import java.io.Serializable;

/**
 * Describes a probe configuration parameter.
 */
public class ProbeConfiguration implements Serializable {
  //the name of the parameter (also the key in key-value pair)
  private String name;
  //describes the parameter in a natural language
  private String description;
  //is this parameter mandatory (can it be left undefined?)
  private boolean mandatory;
  //the value of the parameter
  private String value;

  public ProbeConfiguration(String name, String description, boolean mandatory, Object value) {
    this.name = name;
    this.description = description;
    this.mandatory = mandatory;
    if (value == null) {
      this.value = null;
    } else {
      this.value = value.toString();
    }
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }

  public boolean isMandatory() {
    return mandatory;
  }

  public void setMandatory(boolean mandatory) {
    this.mandatory = mandatory;
  }

  public String getValue() {
    return value;
  }

  public void setValue(String value) {
    this.value = value;
  }

  @Override
  public String toString() {
    return name;
  }

  @Override
  public boolean equals(Object o) {
    if (this == o) return true;
    if (o == null || getClass() != o.getClass()) return false;

    ProbeConfiguration that = (ProbeConfiguration) o;

    if (name != null ? !name.equals(that.name) : that.name != null) return false;

    return true;
  }

  @Override
  public int hashCode() {
    return name != null ? name.hashCode() : 0;
  }
}
